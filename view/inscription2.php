              <p>La constitution du dossier de l’examen :</p>
<p>Pour s’inscrire à un examen du permis de conduire, il faut déposer un dossier auprès du centre d’immatriculation de la résidence du candidat (c’est l’Auto Ecole qui s’en charge).</p>
<p>Le dossier doit être constitué comme suit :</p>
<ul>
<li>Une photocopie certifiée conforme de la carte d’identité nationale, ou le titre de séjour pour l’étranger.</li>
<li>Reçu de paiement des droits de timbre et de rémunération de service institués par la législation en vigueur.</li>
<li>Un certificat médical établi depuis moins de trois mois par un médecin agréé par le Ministre de la Santé Publique et le Ministre des Transports ; ce certificat atteste que le candidat a les capacités requises pour la conduite d’un véhicule (le dossier médical est a récupéré a l’auto-école).</li>
<li>Une attestation de fin de formation pour la candidature à l’examen pour l’obtention de la catégorie sollicitée délivrée par l’établissement d’enseignement de la conduite agrée.</li>
<li>Cinq photos d’identités récentes de face, en couleur, de format 3,5cm x 4,5cm sur fond blanc.</li>
<li>Une photocopie de permis de conduire en cas d’extension.</li>
<li>En cas d’annulation du permis de conduire de l’après période probatoire suite à la perte de la totalité des points, une attestation de suivi de la session d’éducation à la sécurité routière obligatoire.</li>
<li>En cas d’annulation du permis de conduire suite à la perte de la totalité des points, une copie de l’accusé de restitution du permis de conduire au service compétant chargé de la délivrance du permis de conduire.</li>
</ul>
<p>Le dossier doit être déposé au centre d’immatriculation de la résidence du candidat, une convocation lui sera remise fixant la date et l’heure de l’examen.</p>
